CREATE TABLE CONTINENTES (
    ID_CONTINENTE NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(50) NOT NULL UNIQUE
);


CREATE TABLE PAISES (
    ID_PAIS NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(100) NOT NULL UNIQUE,
    ID_CONTINENTE NUMBER NOT NULL,
    CONSTRAINT FK_PAISES_CONTINENTES
        FOREIGN KEY (ID_CONTINENTE)
        REFERENCES CONTINENTES (ID_CONTINENTE)
);


CREATE TABLE VACINAS_OBRIGATORIAS_VIAGEM (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_PAIS NUMBER NOT NULL,
    NOME_vacina VARCHAR2(150) NOT NULL,
    GRUPO_DE_RISCO VARCHAR2(200),
    CONSTRAINT FK_VACINAS_PAISES
        FOREIGN KEY (ID_PAIS)
        REFERENCES PAISES (ID_PAIS)
        ON DELETE CASCADE
)
